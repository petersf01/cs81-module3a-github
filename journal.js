// Greet a user by name
function greet(name) {
  return "Hello, " + name + "!";
}

// this function converts a temperature from Celsius to Fahrenheit
function celsiusToFahrenheit(celsius) {
  return (celsius * 9/5) + 32;
}

// this function checks if a number is even
function isEven(num) {
  return num % 2 === 0;
}

